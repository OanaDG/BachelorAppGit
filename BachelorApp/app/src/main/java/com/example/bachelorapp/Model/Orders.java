package com.example.bachelorapp.Model;

public class Orders {
    private String address, dateTime, email, name, phone, state , totalPrice, username;

    public Orders() {

    }

    public Orders(String address, String dateTime, String email, String name, String phone, String state, String totalPrice, String username) {
        this.address = address;
        this.dateTime = dateTime;
        this.email = email;
        this.name = name;
        this.phone = phone;
        this.state = state;
        this.totalPrice = totalPrice;
        this.username = username;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getDateTime() {
        return dateTime;
    }

    public void setDateTime(String dateTime) {
        this.dateTime = dateTime;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(String totalPrice) {
        this.totalPrice = totalPrice;
    }
}
